# print all the numbers from 0 to 9
for i in range(0, 10):
    print(i, ' ', end='')
print()

# print all the odd numbers from 1 to 9
for i in range(1, 10, 2):
    print(i, ' ', end='')
print()

# print the even numbers starting from 10 
for i in range(10, 1, -2):
    print(i, ' ', end='')
print()


